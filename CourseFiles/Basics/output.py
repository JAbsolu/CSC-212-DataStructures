# Output Variables
# The Python print() function is often used to output variables.

# Example

x = "Python is awesome"
print(x)

# n the print() function, you output multiple variables, separated by a comma:

# Example

x = "Python"
y = "is"
z = "awesome"
print(x, y, z)

# You can also use the + operator to output multiple variables:

# Example

x = "Python "
y = "is "
z = "awesome"
print(x + y + z)

x = 5
y = "John"
print(x, y)